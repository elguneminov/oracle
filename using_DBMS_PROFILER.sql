  SELECT U.RUNID,
         U.UNIT_NUMBER,
         U.UNIT_TYPE,
         U.UNIT_OWNER,
         U.UNIT_NAME,
         D.LINE#,
         D.TOTAL_OCCUR,
         D.TOTAL_TIME,
         D.MIN_TIME,
         D.MAX_TIME
    FROM PLSQL_PROFILER_UNITS U
         JOIN PLSQL_PROFILER_DATA D
             ON U.RUNID = D.RUNID AND U.UNIT_NUMBER = D.UNIT_NUMBER
   WHERE U.RUNID = 1
ORDER BY U.UNIT_NUMBER, D.LINE#;