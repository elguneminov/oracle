CREATE OR REPLACE PROCEDURE DO_SOMETHING (P_TIMES IN NUMBER)
AS
    L_DUMMY   NUMBER;
BEGIN
    FOR I IN 1 .. P_TIMES
    LOOP
        SELECT L_DUMMY + 1 INTO L_DUMMY FROM DUAL;
    END LOOP;
END;


DECLARE
    L_RESULT   BINARY_INTEGER;
BEGIN
    L_RESULT :=
        DBMS_PROFILER.START_PROFILER (
            RUN_COMMENT   => 'do_something: ' || SYSDATE);
    DO_SOMETHING (P_TIMES => 100);
    L_RESULT := DBMS_PROFILER.STOP_PROFILER;
END;

